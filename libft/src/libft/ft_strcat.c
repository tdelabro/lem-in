char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int j;

	if (s1 && s2)
	{
		i = 0;
		j = 0;
		while (s1[i])
			i++;
		while (s2[j])
		{
			s1[i + j] = s2[j];
			j++;
		}
		s1[i + j] = '\0';
	}
	return (s1);
}
